const fs=require('fs');
const path=require('path');
const root=path.resolve(__dirname,'..');
const read=p=>fs.readFileSync(path.join(root,p),'utf8');
const failures=[];
const pass=(ok,msg)=>{if(!ok)failures.push(msg);};
const i18n=read('i18n/index.js');
const global=read('i18n/global-screen-translator.js');
const business=read('i18n/localization-center/business-data.js');
const dashboard=read('i18n/localization-center/dashboard-pilot.js');
const payroll=read('payroll/payroll-core.js');
const release=read('RELEASE_VERSION.txt');
const index=read('index.html');
pass(i18n.includes('var pendingRoots=new Set()'),'Primary mutation queue is not deduplicated.');
pass(i18n.includes('applyActiveSubtrees(lang);'),'Language reapply does not target active surfaces.');
pass(!/function reapplyLanguage[\s\S]{0,500}applyDataAttributes\(lang\);/.test(i18n),'Language reapply still scans all data-i18n nodes.');
pass(!/function init\(\)\{[^}]*installObserver\(\)/.test(global),'Residual translator still installs its own observer during init.');
pass(!/language-changed[\s\S]{0,250}installObserver\(\)/.test(global),'Residual translator reinstalls observer on language change.');
pass(global.includes("mode:'passive-residual-performance'"),'Residual translator mode is not passive.');
pass(business.includes("panel.classList.contains('active')"),'Smart Reports rerender is not active-screen guarded.');
pass(dashboard.includes("el.classList.contains('active')"),'Dashboard rerender is not active-screen guarded.');
pass(payroll.includes("payrollPanel.classList.contains('active')"),'Payroll rerender is not active-screen guarded.');
pass(release.includes('PETATOE v9.4.7')&&release.includes('ELC_V9_4_7_NAVIGATION_LANGUAGE_PERFORMANCE'),'Release metadata mismatch.');
pass(index.includes("PETATOE_RELEASE_VERSION='v9.4.7'")&&index.includes("PETATOE_RELEASE_NAME='ELC_V9_4_7_NAVIGATION_LANGUAGE_PERFORMANCE'"),'index metadata mismatch.');
const result={passed:failures.length===0,checks:11,failures};
fs.writeFileSync(path.join(root,'NAVIGATION_LANGUAGE_PERFORMANCE_RESULTS.json'),JSON.stringify(result,null,2));
if(failures.length){console.error(JSON.stringify(result,null,2));process.exit(1);}console.log(JSON.stringify(result,null,2));
